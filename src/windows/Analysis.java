    private void printImage(JLabel label, String rute){
        this.image = new ImageIcon(rute);
        this.icon = new ImageIcon(
       this.image.getImage().getScaledInstance(
               label.getWidth(),
              label.getHeight(),
               Image.SCALE_DEFAULT
            )
        );
        jLabel1.setIcon(this.icon);
        this.repaint();
    }   

