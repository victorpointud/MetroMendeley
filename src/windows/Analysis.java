    public void AllTitles() {
        List lista = GlobalVariables.getList();
        Node pointer = lista.getHead();
        int index = 0;
        while (pointer != null) {
            String titulo = pointer.getElement();
            Titles.insertItemAt(titulo, index);
            index++;
            pointer = pointer.getNext();
            
        }
    }

